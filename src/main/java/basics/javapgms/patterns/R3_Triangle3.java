package basics.javapgms.patterns;

public class R3_Triangle3 {

    public static void main(String[] args) {
      int   n=3;
        for (int i=0;i<3;i++) {
            for (int j=i; j<n; j++) {

                System.out.print("*");
            }
            System.out.println("");


        }
    }
}

